import type { Attraction, TimeSlot } from '../types';

function makeSlots(startHour: number, endHour: number, intervalMin: number): TimeSlot[] {
  const slots: TimeSlot[] = [];
  let h = startHour;
  let m = 0;
  let i = 0;
  while (h < endHour || (h === endHour && m === 0)) {
    const hh = String(h).padStart(2, '0');
    const mm = String(m).padStart(2, '0');
    const suffix = h < 12 ? 'AM' : 'PM';
    const displayH = h > 12 ? h - 12 : h === 0 ? 12 : h;
    slots.push({
      id: `slot-${i}`,
      label: `${String(displayH).padStart(2, '0')}:${mm} ${suffix}`,
      startTime: `${hh}:${mm}`,
    });
    m += intervalMin;
    if (m >= 60) { h += Math.floor(m / 60); m = m % 60; }
    i++;
  }
  return slots;
}

